class User {
    constructor (image, firstName, lastName, email, phone, street, city, state, postcode, birthdate) {
        this.image = image;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.phone = phone;
        this.street = street;
        this.city = city;
        this.state = state;
        this.postcode = postcode;
        this.birthdate = birthdate;
    }
}